def _sel_counter_field(scenario_short: str, option_name: str) -> str:
    """
    Return a CSS attribute selector for the ``counter-field`` ``<input>`` inside
    the ``<app-phm-counter>`` component scoped to *scenario_short* / *option_name*.

    PHM names the counter host element::

        id="app-collector-{scenario_short}-cnt_coll_op_{option_name}"

    Example::

        _sel_counter_field("MS_Cycling", "Delayed Start (seconds)")
        # → '[id="app-collector-MS_Cycling-cnt_coll_op_Delayed Start (seconds)"] input.counter-field'

    Known *scenario_short* values extracted from PHM DOM (as of 2026-03-05):

    * ``"Manual"``
    * ``"IdleScreenOn"``
    * ``"IdleScreenOff"``
    * ``"ModernStandby"``
    * ``"MS_Cycling"``  ← Modern Standby Cycling
    * ``"VideoPlayback"``
    * ``"VideoStream"``
    """
    host_id = f"app-collector-{scenario_short}-cnt_coll_op_{option_name}"
    return f'[id="{host_id}"] input.counter-field'


def _sel_preset_scenario_radio(name: str) -> str:
    """
    Build a CSS attribute selector for a Preset Scenario radio input.

    PHM uses the naming convention::

        input[type="radio"]  id="chk_coll_sc_<scenario_name>"
        div.form-check-label id="lbl_coll_sc_<scenario_name>"

    Because the scenario name contains spaces the ID must be matched with
    the attribute selector ``[id="..."]`` rather than the ``#id`` shorthand
    (CSS ``#`` syntax requires escaping spaces).

    Args:
        name: The exact scenario label text, e.g. ``"Modern Standby Cycling"``.

    Returns:
        A CSS attribute selector string targeting the radio input.
    """
    return f'[id="chk_coll_sc_{name}"]'


