    def dump_html(self, output_path: Optional[str] = None) -> str:
        """
        Return (and optionally save) the full outer HTML of the current page.

        Use this when a selector fails and you need to inspect the real DOM
        structure to write the correct locator.

        Args:
            output_path: If provided, write HTML to this file path.

        Returns:
            The full page HTML as a string.
        """
        self._require_connected()
        html = self._page.content()
        if output_path:
            resolved = str(Path(output_path).resolve())
            os.makedirs(os.path.dirname(resolved) or '.', exist_ok=True)
            Path(resolved).write_text(html, encoding='utf-8')
            logger.info(f"Page HTML saved to: {resolved}")
            print(f"  [debug] HTML saved → {resolved}")
        return html

    def dump_radio_candidates(self, keyword: str = "") -> list:
        """
        Print all clickable / interactive elements whose visible text contains
        *keyword* (case-insensitive).  Also separately lists every
        ``input[type=radio]`` on the page.

        Useful for diagnosing why ``select_preset_scenario`` cannot find an
        element — run this after navigation and read the printed tag/text/class
        to derive the correct selector.

        Args:
            keyword: Filter string; empty string returns all results.

        Returns:
            List of dicts with keys ``tag``, ``type``, ``text``, ``class``,
            ``id``, ``aria_label``, ``checked``.
        """
        self._require_connected()
        print("\n[debug] ── All input[type=radio] on page ─────────────────")
        radios = self._page.locator('input[type="radio"]').all()
        radio_info = []
        for i, r in enumerate(radios):
            try:
                info = {
                    'index':      i,
                    'id':         r.get_attribute('id') or '',
                    'name':       r.get_attribute('name') or '',
                    'value':      r.get_attribute('value') or '',
                    'aria_label': r.get_attribute('aria-label') or '',
                    'checked':    r.is_checked(),
                    'visible':    r.is_visible(),
                    # text of the parent element (often the label)
                    'parent_text': r.locator('..').inner_text().strip()[:80],
                }
                radio_info.append(info)
                print(f"  radio[{i}] id={info['id']!r:20s} "
                      f"name={info['name']!r:20s} "
                      f"value={info['value']!r:25s} "
                      f"checked={info['checked']} "
                      f"visible={info['visible']} "
                      f"parent_text={info['parent_text']!r}")
            except Exception as exc:
                print(f"  radio[{i}] <error reading attributes: {exc}>")

        kw_lower = keyword.lower()
        print(f"\n[debug] ── Elements with text containing {keyword!r} ───────")
        candidates = self._page.locator(
            'button, label, [role="radio"], [role="option"], '
            'span, div, li'
        ).all()
        matches = []
        for elem in candidates:
            try:
                text = elem.inner_text().strip()
                if kw_lower and kw_lower not in text.lower():
                    continue
                if not text:
                    continue
                info = {
                    'tag':        self._page.evaluate(
                                      '(e) => e.tagName', elem.element_handle()
                                  ).lower(),
                    'text':       text[:80],
                    'class':      elem.get_attribute('class') or '',
                    'id':         elem.get_attribute('id') or '',
                    'role':       elem.get_attribute('role') or '',
                    'aria_label': elem.get_attribute('aria-label') or '',
                }
                matches.append(info)
                print(f"  <{info['tag']}> "
                      f"id={info['id']!r:20s} "
                      f"role={info['role']!r:10s} "
                      f"class={info['class'][:30]!r:32s} "
                      f"text={info['text']!r}")
            except Exception:
                pass
        print("[debug] ─────────────────────────────────────────────────\n")
        return radio_info

