class PHMUIMonitor:
    """
    Web UI monitor for PHM (Powerhouse Mountain).

    Controls PHM via Playwright by driving the web interface at
    ``http://<host>:<port>``.

    Example::

        monitor = PHMUIMonitor(host='localhost', port=1337)
        monitor.wait_for_ready(timeout=30)
        monitor.open_browser(headless=False)
        monitor.navigate_to_collector()
        monitor.set_cycle_count(10)
        monitor.start_test()
        monitor.wait_for_completion(timeout=3600)
        monitor.close_browser()
    """

    def __init__(
        self,
        host: str = 'localhost',
        port: int = 1337,
        headless: bool = False,
        browser_timeout: int = 30000,
    ):
        """
        Initialize PHM UI monitor.

        Args:
            host: PHM web server host (default ``'localhost'``).
            port: PHM web server port (default ``1337``).
            headless: Run browser in headless mode (default ``False``).
            browser_timeout: Playwright default timeout in milliseconds.

        Raises:
            PHMUIError: If playwright is not installed.
        """
        if not _PLAYWRIGHT_AVAILABLE:
            raise PHMUIError(
                "playwright is required for UI monitoring. "
                "Install it with: pip install playwright && playwright install chromium"
            )

        self.host = host
        self.port = port
        self.headless = headless
        self.browser_timeout = browser_timeout
        self.base_url = f"http://{host}:{port}"

        self._playwright = None
        self._browser = None
        self._context = None
        self._page = None
        self._connected = False

    # ------------------------------------------------------------------
    # Server readiness
    # ------------------------------------------------------------------

    def wait_for_ready(self, timeout: int = 30) -> bool:
        """
        Poll until the PHM web server is accepting TCP connections.

        Args:
            timeout: Total seconds to wait.

        Returns:
            True when the server is ready.

        Raises:
            PHMTimeoutError: If not ready within *timeout* seconds.
        """
        deadline = time.time() + timeout
        logger.info(f"Waiting for PHM server at {self.base_url} ...")
        while time.time() < deadline:
            try:
                with socket.create_connection((self.host, self.port), timeout=1):
                    logger.info("PHM server is ready")
                    return True
            except OSError:
                time.sleep(1)

        raise PHMTimeoutError(
            f"PHM server {self.base_url} did not become ready within {timeout}s"
        )

    # ------------------------------------------------------------------
    # Browser lifecycle
    # ------------------------------------------------------------------

    def open_browser(self, headless: Optional[bool] = None) -> None:
        """
        Launch a Playwright Chromium browser and open the PHM web UI.

        Args:
            headless: Override instance headless setting for this call.

        Raises:
            PHMUIError: If browser launch or page load fails.
        """
        if headless is None:
            headless = self.headless

        logger.info(f"Opening Playwright browser (headless={headless})")
        try:
            self._playwright = sync_playwright().start()
            self._browser = self._playwright.chromium.launch(headless=headless)
            self._context = self._browser.new_context()
            self._page = self._context.new_page()
            self._page.set_default_timeout(self.browser_timeout)
            self._page.goto(self.base_url)
            self._page.wait_for_load_state('networkidle')
            self._connected = True
            logger.info(f"Browser opened at {self.base_url}")
        except Exception as exc:
            raise PHMUIError(f"Failed to open browser: {exc}") from exc

    def close_browser(self) -> None:
        """Close the browser and stop Playwright."""
        logger.info("Closing Playwright browser")
        try:
            if self._page:
                self._page.close()
            if self._context:
                self._context.close()
            if self._browser:
                self._browser.close()
            if self._playwright:
                self._playwright.stop()
        except Exception:
            pass
        finally:
            self._page = None
            self._context = None
            self._browser = None
            self._playwright = None
            self._connected = False

    def _require_connected(self) -> None:
        """Raise PHMUIError if browser is not open."""
        if not self._connected or self._page is None:
            raise PHMUIError(
                "Browser not connected. Call open_browser() first."
            )

    # ------------------------------------------------------------------
    # Navigation
    # ------------------------------------------------------------------

    def navigate_to_collector(self) -> None:
        """
        Click the Collector tab in the PHM navigation.

        Raises:
            PHMUIError: If the Collector tab is not found.
        """
        self._require_connected()
        logger.info("Navigating to Collector tab")
        try:
            self._page.click(_SEL_COLLECTOR_TAB)
            self._page.wait_for_load_state('networkidle')
        except PlaywrightTimeoutError as exc:
            raise PHMUIError(
                f"Collector tab not found (selector: {_SEL_COLLECTOR_TAB!r}). "
                "Update _SEL_COLLECTOR_TAB with the real selector."
            ) from exc
        except Exception as exc:
            raise PHMUIError(f"Failed to navigate to Collector: {exc}") from exc

    def is_collection_options_expanded(self) -> bool:
        """
        Return True if the Collection Options panel is already expanded.

        Detection strategy (in order):

        1. ``aria-expanded="true"`` on the toggle element.
        2. The *Preset Scenarios* section is visible — this is the most
           reliable visual indicator because PHM always shows "Preset
           Scenarios" inside the panel when expanded.
        3. Falls back to ``False`` (treat as collapsed) on any error.
        """
        self._require_connected()
        try:
            # Strategy 1: aria-expanded attribute
            toggle = self._page.get_by_text("Collection Options", exact=False).first
            expanded_attr = toggle.get_attribute('aria-expanded', timeout=2000)
            if expanded_attr is not None:
                result = expanded_attr.lower() == 'true'
                logger.debug(f"aria-expanded={expanded_attr!r} → expanded={result}")
                return result
        except Exception:
            pass  # attribute absent or timeout → fall through

        try:
            # Strategy 2: Check if "Preset Scenarios" content is visible
            preset_section = self._page.get_by_text(
                "Preset Scenarios", exact=False
            ).first
            visible = preset_section.is_visible()
            logger.debug(f"'Preset Scenarios' visible={visible} → expanded={visible}")
            return visible
        except Exception:
            pass

        return False  # cannot determine → assume collapsed

    def expand_collection_options(self) -> None:
        """
        Ensure the *Collection Options* accordion is expanded.

        Checks the current state first via :meth:`is_collection_options_expanded`;
        skips the click if the panel is already open.

        Raises:
            PHMUIError: If the Collection Options toggle is not found.
        """
        self._require_connected()
        logger.info("Expanding Collection Options panel")
        try:
            # ── Check current state before clicking ──────────────────
            if self.is_collection_options_expanded():
                logger.info("Collection Options already expanded — skipping toggle")
                return

            # ── Not expanded → click the toggle ──────────────────────
            toggle = self._page.get_by_text(
                "Collection Options", exact=False
            ).first
            toggle.click()
            self._page.wait_for_timeout(400)  # brief pause for animation

            # ── Verify it opened ──────────────────────────────────────
            if self.is_collection_options_expanded():
                logger.info("Collection Options expanded")
            else:
                logger.warning(
                    "Clicked Collection Options toggle but panel may not have opened; "
                    "continuing anyway"
                )
        except PlaywrightTimeoutError as exc:
            raise PHMUIError(
                "Collection Options toggle not found. "
                "Verify the element text matches the real UI label."
            ) from exc
        except PHMUIError:
            raise
        except Exception as exc:
            raise PHMUIError(
                f"Failed to expand Collection Options: {exc}"
            ) from exc

    def select_preset_scenario(self, scenario_name: str) -> None:
        """
        Click the radio button for the named Preset Scenario and verify selection.

        Tries four strategies in order, stopping at the first that results in a
        *checked* radio button.  After each click the radio state is verified —
        this prevents false-positives where a text click hits a heading instead
        of the actual ``<input type="radio">``.

        Strategies:
            1. ``input[type="radio"]`` filtered by nearby text (most precise).
            2. ``label:has-text(name) input[type="radio"]`` — label wraps input.
            3. ``get_by_role("radio", name=name)`` — ARIA accessible name.
            4. ``get_by_label(name)`` — standard ``<label for=...>`` association.

        Args:
            scenario_name: Exact UI label of the scenario, e.g.
                           ``"Modern Standby Cycling"``.

        Raises:
            PHMUIError: If all strategies fail to select the radio button.
        """
        self._require_connected()
        logger.info(f"Selecting preset scenario: '{scenario_name}'")

        def _radio_locator():
            """Primary: radio input by PHM id convention chk_coll_sc_<name>."""
            return self._page.locator(f'[id="chk_coll_sc_{scenario_name}"]')

        def _is_radio_checked(locator) -> bool:
            """Return True if the located element is a checked radio input."""
            try:
                return locator.is_checked(timeout=2000)
            except Exception:
                return False

        # Strategy 1: click the radio input directly (chk_coll_sc_<name>)
        # Strategy 2: click the label div (lbl_coll_sc_<name>) — triggers the radio
        # Strategy 3: get_by_role radio with accessible name
        # Strategy 4: label:has-text > input[radio]
        strategies = [
            (
                f'[id="chk_coll_sc_{scenario_name}"] (radio input)',
                lambda: self._page.locator(f'[id="chk_coll_sc_{scenario_name}"]'),
            ),
            (
                f'[id="lbl_coll_sc_{scenario_name}"] (label div click)',
                lambda: self._page.locator(f'[id="lbl_coll_sc_{scenario_name}"]'),
            ),
            (
                "get_by_role radio",
                lambda: self._page.get_by_role("radio", name=scenario_name).first,
            ),
            (
                "label:has-text > input[radio]",
                lambda: self._page.locator(
                    f'label:has-text("{scenario_name}") input[type="radio"]'
                ).first,
            ),
        ]

        last_exc: Exception = RuntimeError("No strategies were attempted")
        for desc, locator_fn in strategies:
            try:
                locator = locator_fn()
                # Already selected — nothing to do
                if _is_radio_checked(_radio_locator()):
                    logger.info(
                        f"Scenario '{scenario_name}' already selected"
                    )
                    return
                locator.click(timeout=5000)
                self._page.wait_for_timeout(300)
                # Verify the radio is now actually checked
                if _is_radio_checked(_radio_locator()):
                    logger.info(
                        f"Scenario '{scenario_name}' selected and verified "
                        f"via strategy: {desc}"
                    )
                    return
                logger.debug(
                    f"Strategy '{desc}': click succeeded but radio not checked "
                    "— trying next strategy"
                )
            except Exception as exc:
                logger.debug(
                    f"select_preset_scenario strategy '{desc}' failed: {exc}"
                )
                last_exc = exc

        raise PHMUIError(
            f"All strategies failed to select (and verify) preset scenario "
            f"'{scenario_name}'. "
            f"Last error: {last_exc}. "
            "Use browser DevTools to inspect the radio element and update the selector."
        ) from last_exc

    def _fill_counter_field(
        self,
        scenario_short: str,
        option_name: str,
        value: int,
        timeout: int = 10_000,
    ) -> None:
        """
        Robustly set a PHM ``<app-phm-counter>`` field value.

        Waits for the input to become visible and enabled (not disabled), triple-
        clicks to select existing text, fills with *value*, then dispatches
        ``input`` + ``change`` events so Angular's ngModel picks up the change.

        Args:
            scenario_short: PHM short-name for the scenario prefix, e.g. ``"MS_Cycling"``.
            option_name:    Display label of the option, e.g. ``"Delayed Start (seconds)"``.
            value:          Integer value to set.
            timeout:        Milliseconds to wait for the field to become visible.

        Raises:
            PHMUIError: If the counter field cannot be found or filled.
        """
        sel = _sel_counter_field(scenario_short, option_name)
        logger.debug(f"Filling counter field '{option_name}' → {value}  (selector: {sel!r})")
        try:
            loc = self._page.locator(sel)
            loc.wait_for(state="visible", timeout=timeout)
            loc.click(click_count=3)
            loc.fill(str(value))
            loc.dispatch_event("input")
            loc.dispatch_event("change")
            logger.debug(f"Counter field '{option_name}' successfully set to {value}")
        except Exception as exc:
            raise PHMUIError(
                f"Failed to set counter field '{option_name}' "
                f"(selector: {sel!r}): {exc}"
            ) from exc

