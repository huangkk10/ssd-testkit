    def wait_for_completion(self, timeout: int = 3600, poll_interval: int = 5) -> bool:
        """
        Poll the PHM *Show Log* panel until ``"Data analysis finished."`` appears.

        PHM appends log lines to the ``#textArea_daq_logSummary`` textarea while
        the collection is running.  The last line written after a successful
        cycle is ``"Data analysis finished."``.  This method waits for that
        sentinel to appear.

        Args:
            timeout:       Maximum seconds to wait before raising.
            poll_interval: Seconds between each log-text check (default 5).

        Returns:
            ``True`` when the completion marker is detected in the log.

        Raises:
            PHMTimeoutError: If the marker does not appear within *timeout* s.
        """
        self._require_connected()
        logger.info(
            f"Waiting for PHM log marker {_LOG_COMPLETION_MARKER!r} "
            f"(timeout={timeout}s, poll={poll_interval}s)"
        )
        deadline = time.time() + timeout
        last_line_count = 0
        while time.time() < deadline:
            log_text = self.get_log_text()
            lines = [l.strip() for l in log_text.splitlines() if l.strip()]
            # print new lines since last poll
            if len(lines) > last_line_count:
                for line in lines[last_line_count:]:
                    logger.info(f"[PHM Log] {line}")
                last_line_count = len(lines)
            if _LOG_COMPLETION_MARKER in log_text:
                logger.info("PHM log: 'Data analysis finished.' detected — collection complete")
                return True
            time.sleep(poll_interval)

        raise PHMTimeoutError(
            f"PHM test did not complete within {timeout} seconds "
            f"(marker {_LOG_COMPLETION_MARKER!r} not seen in log)"
        )

    def get_log_text(self) -> str:
        """
        Return the current contents of the PHM *Show Log* textarea.

        The textarea (``#textArea_daq_logSummary``) is populated by PHM while
        a collection is running.  Each line is a timestamped status message.

        Returns:
            The full log text as a string (may be empty if nothing has run yet).

        Raises:
            PHMUIError: If the log textarea element is not found.
        """
        self._require_connected()
        try:
            loc = self._page.locator(_SEL_LOG_TEXTAREA)
            loc.wait_for(state="attached", timeout=5_000)
            return loc.input_value()
        except Exception as exc:
            raise PHMUIError(
                f"Failed to read log textarea (selector: {_SEL_LOG_TEXTAREA!r}): {exc}"
            ) from exc

